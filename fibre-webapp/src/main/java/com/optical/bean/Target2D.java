public class Target2D {
    private Integer targetId;

    private Double targetX;

    private Double targetY;

    private Double xSpeed;

    private Double ySpeed;

    private Double xAccSpeed;

    private Double yAccSpeed;

    public Integer getTargetId() {
        return targetId;
    }

    public void setTargetId(Integer targetId) {
        this.targetId = targetId;
    }

    public Double getTargetX() {
        return targetX;
    }

    public void setTargetX(Double targetX) {
        this.targetX = targetX;
    }

    public Double getTargetY() {
        return targetY;
    }

    public void setTargetY(Double targetY) {
        this.targetY = targetY;
    }

    public Double getxSpeed() {
        return xSpeed;
    }

    public void setxSpeed(Double xSpeed) {
        this.xSpeed = xSpeed;
    }

    public Double getySpeed() {
        return ySpeed;
    }

    public void setySpeed(Double ySpeed) {
        this.ySpeed = ySpeed;
    }

    public Double getxAccSpeed() {
        return xAccSpeed;
    }

    public void setxAccSpeed(Double xAccSpeed) {
        this.xAccSpeed = xAccSpeed;
    }

    public Double getyAccSpeed() {
        return yAccSpeed;
    }

    public void setyAccSpeed(Double yAccSpeed) {
        this.yAccSpeed = yAccSpeed;
    }
}
